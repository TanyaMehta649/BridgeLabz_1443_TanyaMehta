//LOGICAL OPERATORS
//1. LOGICAL AND &&
//jab dono condn true hoti h tabhi ans true aaega nahi toh false aaega
console.log(true && true);   // true
console.log(true && false);  // false
console.log(false && true);  // false
console.log(false && false); // false
//2. LOGICAL OR ||
//jab koi b ek condition true hoti h and ans b true aata hai
console.log(true || false);  // true
console.log(false || false); // false
console.log(true || true);   // true
console.log(false || true);  // true
//3. LOGICAL NOT !
//Ye condition ko not bnata h
//true ko false krta h and false ko true
console.log(!true);
//will return false
console.log(!false);
//will return true
